#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────────────
//  BWR Instagram reel — "Témoignage" (calm, realist, soft music, no voice)
//
//  Records the self-contained reel page `public/ads/promo-voix.html`, which
//  plays its own ~30 s cinematic timeline (slow b-roll + subtitles). Steps:
//    1. open the page headlessly in bare mode (no controls/frame),
//    2. render the page's OWN synth music offline to a WAV (Playwright doesn't
//       capture live WebAudio, so we ask the page for the exact track via
//       window.renderMusicWav()), then
//    3. press ▶, let the timeline run, and
//    4. mux the WAV in and encode three Instagram-ready MP4s.
//
//  No external/licensed audio needed — the soft acoustic bed is generated by
//  the page itself, so this build is fully self-contained.
//
//  Outputs (WITH music):
//    • 9:16 (1080×1920) — Reels/Stories → scripts/promo/out/bwr-temoignage-9x16.mp4
//    • 1:1  (1080×1080) — feed square    → bwr-temoignage-1x1.mp4
//    • 4:5  (1080×1350) — feed portrait  → bwr-temoignage-4x5.mp4
//
//  Usage (dev server must be running: npm run dev:worker):
//    PROMO_BASE_URL=http://localhost:8787 node scripts/promo/make-temoignage.mjs
//    node scripts/promo/make-temoignage.mjs --encode-only   # re-use last recording
//    node scripts/promo/make-temoignage.mjs --only=9x16       # one format only
// ─────────────────────────────────────────────────────────────────────────────

import { chromium } from '@playwright/test';
import ffmpegPath from 'ffmpeg-static';
import { spawn } from 'node:child_process';
import { mkdir, rm, readdir, rename, writeFile } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_DIR = join(__dirname, 'out');
const RAW_DIR = join(__dirname, '.raw-temoignage');
const MASTER = join(OUT_DIR, 'bwr-temoignage-master.webm');
const LOOP   = join(OUT_DIR, 'bwr-temoignage-loop.wav'); // exact synth bed from the page
const BED    = join(OUT_DIR, 'bwr-temoignage-bed.m4a');  // trimmed/faded/normalized bed

const BASE_URL = process.env.PROMO_BASE_URL || 'http://localhost:8787';
const PAGE = '/ads/promo-voix.html?bare=1'; // full-bleed capture mode (no controls/frame)

// The page's own timeline length (kept in sync with TOTAL + tail in promo-voix.html).
const REEL_MS = 31500;
const AUDIO_SECONDS = 44; // render a little longer than the reel; ffmpeg trims to fit
// Final clip length. The reel plays in real time and ends (CTA card) by ~30 s;
// Chromium pads the recorded WebM with a frozen tail and a bogus duration, so we
// don't trust it — we trim both video and music to this fixed length instead.
const FINAL_SEC = 31.5;

const VIEWPORT = { width: 540, height: 960 };
const SCALE = 2;

const args = process.argv.slice(2);
const ENCODE_ONLY = args.includes('--encode-only');
const ONLY = (args.find(a => a.startsWith('--only=')) || '').split('=')[1] || null;

const FORMATS = [
  { id: '9x16', w: 1080, h: 1920, mode: 'native' },
  { id: '1x1',  w: 1080, h: 1080, mode: 'blurpad' },
  { id: '4x5',  w: 1080, h: 1350, mode: 'blurpad' },
];

// ── record the self-contained reel page (no cursor — it's headless) ──────────
async function record() {
  console.log(`▶ recording ${BASE_URL}${PAGE} …`);
  await rm(RAW_DIR, { recursive: true, force: true });
  await mkdir(RAW_DIR, { recursive: true });
  await mkdir(OUT_DIR, { recursive: true });

  const browser = await chromium.launch();
  const ctxOpts = {
    baseURL: BASE_URL, viewport: VIEWPORT, deviceScaleFactor: SCALE,
    isMobile: true, hasTouch: true, locale: 'fr-FR',
  };

  // PASS 1 — render the page's OWN music bed offline → WAV, in a context WITHOUT
  // recording, so the (slow) render doesn't add dead air to the video.
  {
    const audioCtx = await browser.newContext(ctxOpts);
    const ap = await audioCtx.newPage();
    await ap.goto(PAGE, { waitUntil: 'domcontentloaded', timeout: 30000 });
    const b64 = await ap.evaluate(s => window.renderMusicWav(s), AUDIO_SECONDS);
    if (!b64) throw new Error('renderMusicWav() returned nothing (OfflineAudioContext unavailable?)');
    await writeFile(LOOP, Buffer.from(b64, 'base64'));
    await audioCtx.close();
    console.log(`✔ music bed rendered → ${LOOP}`);
  }

  // PASS 2 — record the reel cleanly. Hide the gate FIRST so the start screen
  // never appears, let the b-roll paint, then drive the page's timeline.
  const context = await browser.newContext({
    ...ctxOpts,
    recordVideo: { dir: RAW_DIR, size: { width: VIEWPORT.width, height: VIEWPORT.height } },
  });
  const page = await context.newPage();
  await page.goto(PAGE, { waitUntil: 'domcontentloaded', timeout: 30000 });
  await page.evaluate(() => { const g = document.getElementById('gate'); if (g) g.style.display = 'none'; });
  await page.waitForTimeout(1200); // let fonts + the first photo paint
  await page.evaluate(() => play());
  await page.waitForTimeout(REEL_MS + 1200);

  await context.close(); // finalizes the .webm
  await browser.close();

  const files = (await readdir(RAW_DIR)).filter(f => f.endsWith('.webm'));
  if (!files.length) throw new Error('no video was recorded');
  if (existsSync(MASTER)) await rm(MASTER);
  await rename(join(RAW_DIR, files[0]), MASTER);
  await rm(RAW_DIR, { recursive: true, force: true });
  console.log(`✔ master recorded → ${MASTER}`);
}

// ── ffmpeg plumbing ──────────────────────────────────────────────────────────
function run(a) {
  return new Promise((resolve, reject) => {
    const p = spawn(ffmpegPath, a, { stdio: ['ignore', 'ignore', 'pipe'] });
    let err = '';
    p.stderr.on('data', d => { err += d; });
    p.on('close', code => code === 0 ? resolve(err) : reject(new Error(err.slice(-1500))));
  });
}

// ── music bed (the page's own soft synth track, trimmed + faded + normalized) ─
async function buildMusic(duration) {
  if (!existsSync(LOOP)) throw new Error(`music loop not found (${LOOP}) — run without --encode-only first`);
  const fadeStart = Math.max(0, duration - 2.0);
  console.log(`▶ preparing ${duration.toFixed(1)}s soft-music bed …`);
  await run([
    '-y', '-stream_loop', '-1', '-i', LOOP,
    '-t', String(duration),
    '-af', `afade=t=in:st=0:d=0.6,afade=t=out:st=${fadeStart}:d=2.0,loudnorm=I=-16:TP=-1.5:LRA=11`,
    '-c:a', 'aac', '-b:a', '192k', BED,
  ]);
  console.log(`✔ music bed → ${BED}`);
}

// ── encode (video + music) ───────────────────────────────────────────────────
async function encode() {
  if (!existsSync(MASTER)) throw new Error(`master not found (${MASTER}) — run without --encode-only first`);
  if (!existsSync(BED)) throw new Error(`music bed not found (${BED})`);
  const targets = ONLY ? FORMATS.filter(f => f.id === ONLY) : FORMATS;
  if (!targets.length) throw new Error(`unknown --only value: ${ONLY}`);

  for (const f of targets) {
    const out = join(OUT_DIR, `bwr-temoignage-${f.id}.mp4`);
    const common = [
      '-c:v', 'libx264', '-preset', 'medium', '-crf', '20',
      '-pix_fmt', 'yuv420p', '-profile:v', 'high', '-level', '4.0',
      '-colorspace', 'bt709', '-color_primaries', 'bt709', '-color_trc', 'bt709',
      '-r', '30', '-c:a', 'aac', '-b:a', '192k', '-ar', '48000', '-ac', '2',
      '-t', String(FINAL_SEC), // hard-trim the frozen WebM tail
      '-shortest', '-movflags', '+faststart', out,
    ];
    let a;
    if (f.mode === 'native') {
      a = ['-y', '-i', MASTER, '-i', BED,
        '-vf', `scale=${f.w}:${f.h}:flags=lanczos`,
        '-map', '0:v', '-map', '1:a', ...common];
    } else {
      a = ['-y', '-i', MASTER, '-i', BED,
        '-filter_complex',
        `[0:v]split=2[bg][fg];` +
        `[bg]scale=${f.w}:${f.h}:force_original_aspect_ratio=increase,crop=${f.w}:${f.h},` +
        `gblur=sigma=22,eq=brightness=-0.12:saturation=1.05[bgb];` +
        `[fg]scale=${f.w}:${f.h}:force_original_aspect_ratio=decrease:flags=lanczos[fgs];` +
        `[bgb][fgs]overlay=(W-w)/2:(H-h)/2[v]`,
        '-map', '[v]', '-map', '1:a', ...common];
    }
    process.stdout.write(`▶ encoding ${f.id} (${f.w}×${f.h}) … `);
    await run(a);
    console.log('done →', out);
  }
}

(async () => {
  try {
    if (!ENCODE_ONLY) await record();
    const duration = FINAL_SEC; // fixed length — the WebM duration is unreliable
    if (!ENCODE_ONLY || !existsSync(BED)) await buildMusic(duration);
    await encode();
    console.log('\n✅ Témoignage reel ready in scripts/promo/out/ — bwr-temoignage-9x16.mp4 drops straight into Instagram.');
  } catch (e) {
    console.error('\n❌ témoignage reel build failed:', e.message);
    process.exit(1);
  }
})();
